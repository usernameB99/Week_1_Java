package Woche1;

public class A3_TaschenRechnerV1 {
    public static void main(String[] args) {

        int a = 5;
        int b = 3;

        System.out.println("Erste Variable: " + a);
        System.out.println("Zweite Variable: " + b);

        System.out.println(a + " + " + b + " = " + (a + b));
        System.out.println(a + " - " + b + " = " + (a - b));
        System.out.println(a + " * " + b + " = " + (a * b));
        System.out.println(a + " / " + b + " = " + ((double)a / b));
        System.out.println(a + " % " + b + " = " + (a % b));




    }
}
